package com.example.myfirstkotlinapplication

public class AndroidVersion {
    private val ver: String? = null
    private val name: String? = null
    private val api: String? = null

    fun getVer(): String? {
        return ver
    }

    fun getName(): String? {
        return name
    }

    fun getApi(): String? {
        return api
    }
}